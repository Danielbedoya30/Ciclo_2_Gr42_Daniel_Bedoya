    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        // TODO add your handling code here:
        try{

            String nombre = txt_name.getText();
            String tipo_documento = cb_tipo_documento.getSelectedItem().toString();
            int num_documento = Integer.parseInt(txt_num_documento.getText());
            String direccion = txt_direccion.getText();
            String telefono = txt_telefono.getText();
            String email = txt_email.getText();

            //System.out.println(nombre + "-" + tipo_documento + "--" + num_documento + "--" + direccion + "--" + telefono + "--" + email);

            if(nombre.equals("")){
                JOptionPane.showMessageDialog(this, "Por favor ingrese el nombre");
            }else{
                ClientClass cliente = new ClientClass(nombre, tipo_documento, num_documento, direccion, telefono, email);
                client_list.add(cliente);
                anadir_client();
                JOptionPane.showMessageDialog(this, "El cliente ha sido registrado con exito");
                limpiar_campos();
            }

        }catch (Exception e){
            JOptionPane.showMessageDialog(this, "Recuerde que debe ingresar un numero  para el documento");
        }

    }//GEN-LAST:event_btn_addActionPerformed
     
   
