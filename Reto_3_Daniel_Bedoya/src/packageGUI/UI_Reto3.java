    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        int buscar_document = Integer.parseInt(txt_num_document_search.getText());
        boolean eliminar_cliente = false;
        for (ClientClass client : client_list) {
            if (buscar_document == client.getNumero_documento()) {
                client_list.remove(client);
                this.anadir_client();
                eliminar_cliente = true;
                JOptionPane.showMessageDialog(this, "Informacion eliminada con exito");
                break;
            }

        }if(!eliminar_cliente){
            JOptionPane.showMessageDialog(this,"El numero de documento: "+ buscar_document + " No pertenece a ningun cliente registrado" );
        }

    }//GEN-LAST:event_btn_deleteActionPerformed

    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        // TODO add your handling code here:
        int buscar_document = Integer.parseInt(txt_num_document_search.getText());
        boolean editar_cliente = false;

        for (ClientClass client : client_list) {
            if (buscar_document == client.getNumero_documento()) {

                String edit_name = txt_edit_name.getText();
                client.setNombre(edit_name);

                String edit_dir = txt_edit_direcc.getText ();
                client.setDireccion(edit_dir);

                String edit_tel = txt_edit_tel.getText();
                client.setTelefono(edit_tel);

                String edit_email = txt_edit_email.getText ();
                client.setEmail(edit_email);

                this.anadir_client();
                editar_cliente = true;
                JOptionPane.showMessageDialog(this, "Informacion actualizada con exito");
                break;
            }

        }if(!editar_cliente){
            JOptionPane.showMessageDialog(this,"El numero de documento: "+ buscar_document + " No pertenece a ningun cliente registrado" );
        }

    }//GEN-LAST:event_btn_editActionPerformed

    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
        // TODO add your handling code here:
        int buscar_document = Integer.parseInt(txt_num_document_search.getText());
        boolean encontrar_cliente = false;

        for (ClientClass client : client_list) {
            if (buscar_document == client.getNumero_documento()) {
                jl_search.setText("<html> Nombre: " + client.getNombre() + "<br/>Tipo de documento: " + client.getTipo_documento() + "<br/>Direccion: " + client.getDireccion() + "<br/>Telefono: " + client.getTelefono() + "<br/>Email: " + client.getEmail() + "</html>");

                String edit_name = client.getNombre();
                txt_edit_name.setText(edit_name);

                String edit_tipo_doc = client.getTipo_documento();
                cb_edit_tipo_doc.setSelectedItem(edit_tipo_doc);

                String edit_dir = client.getDireccion();
                txt_edit_direcc.setText(edit_dir);

                String edit_tel = client.getTelefono();
                txt_edit_tel.setText(edit_tel);

                String edit_email = client.getEmail();
                txt_edit_email.setText(edit_email);

                encontrar_cliente = true;

            }

        } if(!encontrar_cliente){
            JOptionPane.showMessageDialog(this,"El numero de documento: "+ buscar_document + " No pertenece a ningun cliente registrado" );
        }
    }//GEN-LAST:event_btn_searchActionPerformed

